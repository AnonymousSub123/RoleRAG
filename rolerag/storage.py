@dataclass
class JsonKVStorage(BaseKVStorage):
    def __post_init__(self):
        working_dir = self.global_config["working_dir"]
        self._file_name = os.path.join(working_dir, f"kv_store_{self.namespace}.json")
        self._data = load_json(self._file_name) or {}
        logger.info(f"Load KV {self.namespace} with {len(self._data)} data")

    async def all_keys(self) -> list[str]:
        return list(self._data.keys())

    async def index_done_callback(self):
        write_json(self._data, self._file_name)

    async def get_by_id(self, id):
        return self._data.get(id, None) # A value to return if the specified key does not exist.

    async def get_by_ids(self, ids, fields=None):
        if fields is None:
            return [self._data.get(id, None) for id in ids]
        return [
            (
                {k: v for k, v in self._data[id].items() if k in fields}
                if self._data.get(id, None)
                else None
            )
            for id in ids
        ]

    async def filter_keys(self, data: list[str]) -> set[str]:
        return set([s for s in data if s not in self._data])

    async def upsert(self, data: dict[str, dict]):
        left_data = {k: v for k, v in data.items() if k not in self._data}
        self._data.update(left_data)
        return left_data

    async def drop(self):
        self._data = {}

@dataclass
class DataFrameDBStorage(BaseDFrameStorage):

    def __post_init__(self):
        working_dir = self.global_config["working_dir"]
        self._file_name = os.path.join(working_dir, f"dataframe_storage_{self.namespace}.csv")
        if os.path.exists(self._file_name):
            self._data = pd.read_csv(self._file_name, index_col = False)
        else:
            self._data = pd.DataFrame(columns = self.columns)

        logger.info(f"Load DataFrame {self.namespace} with {len(self._data)} data")

    async def index_done_callback(self):
        self._data.to_csv(self._file_name, index = None)

    async def filter_by_column_values(self, data_list: list[str], column:str) -> set[str]:
        return [item for item in data_list if item not in self._data[column].values]

    async def get_by_column_value(self, column_name:str, value:str)->pd.DataFrame:
        return self._data[self._data[column_name] == value]

    async def get_column_by_another(self, src_col:str, values:list, tgt_col:str)->list:
        return self._data[self._data[src_col].isin(values)][tgt_col]

    async def get_by_multiple_column_values(self, column_names:list[str], values:list[Any])->pd.DataFrame:

        assert len(column_names) == len(values), "Two arguments have different lengths"
        return self._data[
            pd.concat([(self._data[column_names[i]] == values[i]) for i in range(len(column_names))], axis=1).all(axis=1)
        ]

    async def upsert(self, extracted_dialogue:list[dict]):
        """
        Insert the extracted dialogue into the table.
        [speaker, attendee, dialogue, chunk_id, doc_id]
        """
        self._data = pd.concat([self._data, pd.DataFrame(extracted_dialogue)], ignore_index = True)

